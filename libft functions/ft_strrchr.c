#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s) + 1;
	while (len > 0)
	{
		if (s[len - 1] == (char)c)
			return ((char *)s + (len - 1));
		len--;
	}
	return (NULL);
}
